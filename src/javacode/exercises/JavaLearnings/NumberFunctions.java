package javacode.exercises.JavaLearnings;

public class NumberFunctions {
    private int number1;
    private int number2;
    private int number3;
    private int number4;
    private int number5;

    public void setNumber1(int number1) {
        this.number1 = number1;
    }

    public void setNumbers(int number1, int number2, int number3, int number4, int number5) {
        this.number1 = number1;
        this.number2 = number2;
        this.number3 = number3;
        this.number4 = number4;
        this.number5 = number5;
    }

    public void iterateNumbers(int number) {
        this.number1 =+ number;
        this.number2 =+ number;
        this.number3 =+ number;
        this.number4 =+ number;
        this.number5 =+ number;
    }

}
